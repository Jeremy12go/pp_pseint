package Clases;

public class Vertice {
    //cambiar diagrama de clases tipo de variable
    public double x;
    public double y;

    public Vertice(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}

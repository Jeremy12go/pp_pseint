package Clases;

public class Arista {
    public double ancho;
    public double alto;

    public Arista(double ancho, double alto) {
        this.ancho = ancho;
        this.alto = alto;
    }

    public double getAncho() {
        return ancho;
    }

    public double getAlto() {
        return alto;
    }
}
